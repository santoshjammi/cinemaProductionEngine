class TestAgentFunctionsUseProvidedURL:
    """Verify the agent functions actually USE the provided base_url/api_key
    (not just accept them as unused kwargs)."""

    def test_generate_dna_uses_provided_url(self):
        """If the URL is unreachable, generate_dna should fail — proving it
        tried to connect to the provided URL, not the default."""
        try:
            generate_dna(
                "test synopsis",
                base_url="http://nonexistent-host-abc123.invalid",
                api_key="test",
            )
            assert False, "Expected LLMError"
        except Exception as e:
            # The error must come from trying to reach the nonexistent host
            err = str(e)
            assert "LMStudio" in err or "nodename" in err or "unreachable" in err.lower() or "Errno" in err


class TestPipelineLLMConfig:
    """Verify the pipeline's CLI args / manifest overrides work."""

